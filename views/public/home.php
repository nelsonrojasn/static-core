<h1>static-core</h1>
<p>Menos es más.</p>

<p>
Este framework no intenta hacerlo todo.<br>
Te entrega lo esencial para que tú construyas el resto.
</p>

<h3>Cómo empezar</h3>
<ul>
  <li>Edita tus rutas en <code>public/index.php</code></li>
  <li>Crea un handler en <code>src/Public/</code></li>
  <li>Renderiza una vista desde <code>views/</code></li>
  <li>Levanta el servidor:
    <pre>php -S localhost:8000 -t public</pre>
  </li>
</ul>

<p>
Si algo parece simple… es porque debe serlo.
</p>
