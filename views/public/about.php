<h1>About static-core</h1>

<p>
static-core es un recordatorio:<br>
<strong>el software no necesita ser complejo para ser potente.</strong>
</p>

<p>
Este framework existe para mostrar que seis piezas bien pensadas pueden reemplazar cientos de líneas de magia.
Su meta no es impresionar, sino <em>aclarar</em>.
</p>

<p>
Aquí, cada desarrollador construye sobre un terreno limpio.
Sin capas innecesarias.
Sin ruido.
Sin temor a romper algo que no entiende.
</p>

<p><em>Programar es pensar. static-core solo despeja el camino.</em></p>
